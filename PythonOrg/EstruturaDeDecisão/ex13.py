dia = int(input('Informe o dia da semana:'))

if dia == 1:
    print(f'Domingo')
elif dia == 2:
    print('Segunda')
elif dia == 3:
    print('Terça-feira')
elif dia == 4:
    print('Quarta-feira')
elif dia == 5:
    print('Quinta-feira')
elif dia == 6:
    print('Sexta-feira')
elif dia == 7:
    print('Domingo')
else:
    print('Valor Inválido')